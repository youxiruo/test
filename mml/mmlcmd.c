#include "mmlcmd.h"


//------------------------------------------------------------------
//	判断一个字符是否是空格
//	返回	1 是空格
//	返回	0 不是
//------------------------------------------------------------------
int IsSpace(char c)
{
    if (c==' ' || c==9) return 1;
    return 0;
}

//------------------------------------------------------------------
//	判断一个字符是否是字母
//	返回	1 是
//		0 否
//------------------------------------------------------------------
int IsAlpha(char c)
{
    if (c>='A' && c<='Z') return 1;
    if (c>='a' && c<='z') return 1;
    if (c=='_' || c=='?' || c=='@' || c=='#' || c=='$' || c=='*') return 1;
    return 0;
}

//------------------------------------------------------------------
//	判断一个字符是否是十进制数（0-9）
//	返回	1 是
//		0 否
//------------------------------------------------------------------
int IsDecDigit(char c)
{
    if (c>='0' && c<='9') return 1;
    return 0;
}

//------------------------------------------------------------------
//	判断一个字符是否是十六进制数（0-9，A-F）
//	返回	1 是
//		0 否
//------------------------------------------------------------------
int IsHexDigit(char c)
{
    if (c>='0' && c<='9') return 1;
    if (c>='A' && c<='F') return 1;
    if (c>='a' && c<='f') return 1;
    return 0;
}

//------------------------------------------------------------------
//	判断一个字符是否是终止符（\x00 或 ;）
//	返回	1 是
//		0 否
//------------------------------------------------------------------
int IsTermChar(char c)
{
    if (c==0 || c==';') return 1;
    return 0;
}

